%punto 1

t=-8:0.001:8;
xt=(-2<t).*(t<-1)+(-1)*(-1<t).*(t<=0)+(t-1).*(0<t).*(t<1)+(1<t).*(t<2);
plot(t,xt)
%ylim([-1.2 1.2])
title('x(t)')
%x(-2t+1)
xT=(-2<(-0.5*t-1)).*((-0.5*t-1)<-1)+(-1)*(-1<(-0.5*t-1)).*((-0.5*t-1)<=0)+((-0.5*t-1)-1).*(0<(-0.5*t-1)).*((-0.5*t-1)<1)+(1<(-0.5*t-1)).*((-0.5*t-1)<2);
figure
plot(t,xT)
% ylim([-1.2 1.2])
title('x(-0.5t-1)')
%x(-(-2t+1))
xTi=(-2<(0.5*t-1)).*((0.5*t-1)<-1)+(-1)*(-1<(0.5*t-1)).*((0.5*t-1)<=0)+((0.5*t-1)-1).*(0<(0.5*t-1)).*((0.5*t-1)<1)+(1<(0.5*t-1)).*((0.5*t-1)<2);
figure
plot(t,xTi)
% ylim([-1.2 1.2])
title('x((0.5t-1))')
%Parte par
xe=(xT+xTi)/2;
figure
plot(t,xe)
% ylim([-1.2 1.2])
title('xe(t)')

%Parte impar
xo=(xT-xTi)/2;
figure
plot(t,xo)
% ylim([-1.2 1.2])
title('xo(t)')
